/**
 * @author: Zc
 * @description: 将一个非严格递增的二维数组看作一个从第一行最后一列为根节点的二叉搜索树即可。
 * @date: 2025/4/3 23:08
 * @param null
 * @return
 */
class Solution {
    public boolean searchMatrix(int[][] matrix, int target) {
        int row = 0, col = matrix[0].length - 1;
        while (row < matrix.length && col >= 0) {
            if (matrix[row][col] == target) {
                return true;
            }
            if (matrix[row][col] < target) {
                row++;
            } else {
                col--;
            }
        }
        return false;
    }
}
