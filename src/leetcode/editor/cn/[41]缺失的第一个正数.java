/**
 * @author: Zc
 * @description: 数组缺失的第一个正数，nums.length 的数组缺失的第一个正数只会出现在 [1,N+1] 的范围内；
 *               可以理解为当前数组中小于等于 nums.length && 大于等于 1 的值都在正确位置上时，遍历数组，
 *               首先遇到不符合条件的位置即为缺失值，即 index + 1；
 * @date: 2025/3/12 14:27
 * @param null
 * @return
 */
class Solution {
    public int firstMissingPositive(int[] nums) {
        for (int i = 0; i < nums.length; i++) {
            while (nums[i] >= 1 && nums[i] <= nums.length && nums[nums[i] - 1] != nums[i]) {
                int temp = nums[i];
                nums[i] = nums[temp - 1];
                nums[temp - 1] = temp;
            }
        }

        for (int i = 0; i < nums.length; i++) {
            if (nums[i] != i + 1) {
                return i + 1;
            }
        }

        return nums.length + 1;
    }
}
